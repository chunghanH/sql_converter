    private void paste_textActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paste_textActionPerformed
        Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
        Transferable contents = clipboard.getContents(null);
        try {
            String result = (String)contents.getTransferData(DataFlavor.stringFlavor);
            input_text.setText(result);
        }
        catch (Exception e){
            System.out.println(e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_paste_textActionPerformed

    private void copy_textActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_copy_textActionPerformed
        Toolkit toolkit = Toolkit.getDefaultToolkit();
        Clipboard clipboard = toolkit.getSystemClipboard();
        StringSelection strSel = new StringSelection((String)output_text.getText());
        clipboard.setContents(strSel, null);
    }//GEN-LAST:event_copy_textActionPerformed

