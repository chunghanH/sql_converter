    private void convertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertActionPerformed
        String st = (String)statement_type.getSelectedItem();
        String vt = (String)variable_type.getSelectedItem();
        String vn = (String)variable_name.getText();
        String text = (String)input_text.getText();
        String tn = (String)table_name.getText();
        StatementMaker sm = new StatementMaker(st,vt,vn,text,tn);
        if(vn.trim().length()==0){
           output_text.setText("Variable Name can't be blank!!!"); 
        }
        else{
           output_text.setText(sm.getStatement()); 
        }
    }//GEN-LAST:event_convertActionPerformed

